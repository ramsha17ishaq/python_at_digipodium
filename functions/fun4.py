# create a function for adding value
def add(num1,num2):
    a = num1 + num2
    return a
if __name__ == "__main__":
    print(add(2,4))
# create a function for subtracting value
def sub(num1,num2):
    b = num1 - num2
    return b
if __name__ == "__main__":
    print(sub(5,2))
# create a function for multiplying value
def mul(num1,num2):
    c = num1 * num2
    return c
if __name__ == "__main__":
    print(mul(3,4))
# create a function for dividing value
def div(num1,num2):
    d = num1 / num2
    return d
if __name__ == "__main__":
    print(div(8,2))